As part of my engagements with key stakeholders, I had a fruitful interaction with the Council of Indigenous Business Association in Accra today. CIBA, which is the largest informal business group in Ghana, contributes significantly to job creation and the development of our economy. However, many of their members are excluded in so many things as they are not formalised. I, therefore, encouraged members who have not yet registered their businesses to do so, in order to benefit from government initiatives, which are aimed at boosting their businesses. I also explained to them, in detail, my proposed tax reforms, including the business-friendly proposal for 
tax amnesty for businesses and individuals, as well as a flat tax rate, to support the growth of businesses. He Said.                            </p>
