<br>12.Production or activities involving harmful or exploitative forms of forced labour
<br>13.Child labour
<br>14.Commercial logging operations for use in primary tropical moist forest
<br>15.Production or trade in products containing Polychlorinated biphenyls (PCBs). <br>16.Production or trade in ozone depleting substances subject to international phase out.
