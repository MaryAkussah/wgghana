FEMALE OWNED BUSINESS ARE ENCOURAGED TO APPLY
OPPORTUNITY<br>
Selected beneficiaries will receive the following: Practical Training for business growth Provision of financial management system Technical Support Potential for grant funding after the acceleration stage.
<br>
<br>
<br>
APPLY NOW<br><br>
ELIGIBILITY<br>
1.MSME must be a registered Ghanaian company (51% or more shareholding must be Ghanaian)<br>
2. Must be registered as a Limited Liability Company, Partnership or Sole Proprietorship Must provide two years management account.<br>
3. Export-ready/exporting experience that can be demonstrated through past export sales is an advantage.<br>
<br>

EXCLUSION LIST<br>
1.Activities involving conversion of natural habitats/ecologically sensitive areas and/or damaging to national monuments, non-replicable cultural properties.
<br>2.Production or trade in any product or activity deemed illegal under The Republic of Ghana's laws or regulations or international conventions and agreements.
<br>3.Production or trade in alcoholic beverages including country made liquor.
<br>4.Gambling, casinos and equivalent enterprises.
<br>5.Trade in wildlife or wildlife products regulated under Convention on International Trade in Endangered Species.
<br>6.Production or trade in radioactive materials.
<br>7.Production or trade in or use of unbounded asbestos fibers.
<br>8.Purchase of logging equipment for use in cutting forest.
<br>9.Production or trade in pharmaceuticals subject to international phase outs or bans.
